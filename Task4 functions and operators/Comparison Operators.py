# Comparison Operators:
num1 = 25
num2 = 17
print("\n")
print("Comparison Operators:")

result = num1 > num2
print("Num1 is Greater than Num 2: ", result)

result = num1 < num2
print("Num1 is Less than Num 2: ", result)

result = num1 == num2
print("Num1 is Equal to Num 2: ", result)

result = num1 >= num2
print("Num1 is Greater than or equal to Num 2: ", result)

result = num1 <= num2
print("Num1 is Less than or equal to Num 2: ", result)

result = num1 != num2
print("Num1 is not euqal to Num 2: ", result)
print("\n")
